import type { Wall } from './types'
import { WORLD_W, WORLD_H } from './constants'

// 골라인 y좌표 (구슬이 이 선에 닿으면 완료 처리)
export const goalY = WORLD_H * 0.96  // 1440

// ─── 벽 정의 ────────────────────────────────────────────────────────────
// Wall = { x1, y1, x2, y2 } 선분 하나
// 좌표계: x 0(왼쪽) ~ 280(오른쪽), y 0(위) ~ goalY(골라인)
// ────────────────────────────────────────────────────────────────────────
export const walls: Wall[] = (() => {
  const result: Wall[] = []

  // x축: 상단 통로 중앙 정렬을 위해 우측으로 이동 (통로 중심 105.5 → 140)
  const offsetX = 34
  const s = (pts: [number, number][]) => pts.map(([x, y]): [number, number] => [x + offsetX, y])
  const seg = (pts: [number, number][]) => {
    for (let i = 0; i < pts.length - 1; i++)
      result.push({ x1: pts[i][0], y1: pts[i][1], x2: pts[i+1][0], y2: pts[i+1][1] })
  }

  // ── 오른쪽 외벽 ──────────────────────────────────────────────────────
  // SVG path 오른쪽 윤곽: (152,0)→…→(197,648.5)→(152,648.5)→(127,569)→(104,569)→(82,648.5)
  //   →(197,648.5)→(152,790)→(152,996)→(116,1021.5)→(152,1038.5)→(152,1115)→(197,1165.5)→(197,1276)→(116,1360)→(116,1500)
  seg(s([
    [152,    0],
    [152,  183],
    [ 92.5, 294],
    [ 92.5, 329.5],
    [152,   401],
    [152,   489.5],
    [197,   648.5],
  ]))
  // 내부 홈 (오른쪽): 648.5 구간 — 중앙 꼭짓점으로 V자 형태
  seg(s([
    [152,  648.5],
    [116,  569],
    [ 82,  648.5],
  ]))
  seg(s([
    [197,  648.5],
    [152,  790],
    [152,  996],
    [116, 1021.5],
    [152, 1038.5],
    [152, 1115],
    [197, 1165.5],
    [197, 1276],
    [116, 1360],
    [116, 1500],
  ]))

  // ── 왼쪽 외벽 ───────────────────────────────────────────────────────
  // SVG path 왼쪽 윤곽: (59,0)→(59,183)→(0,294)→(0,372.5)→(92.5,401)→(92.5,489.5)
  //   →(46.5,552.5)→(20,648.5)→(82,711.5)→(20,790)→(20,906)→(59,924)→(20,942)→(20,1021.5)
  //   →(0,1073.5)→(0,1276)→(96,1360)→(96,1500)
  seg(s([
    [ 59,    0],
    [ 59,  183],
    [  0,  294],
    [  0,  372.5],
    [ 92.5, 401],
    [ 92.5, 489.5],
    [ 46.5, 552.5],
    [ 20,  648.5],
    [ 82,  711.5],
    [ 20,  790],
    [ 20,  906],
    [ 59,  924],
    [ 20,  942],
    [ 20, 1021.5],
    [  0, 1073.5],
    [  0, 1276],
    [ 96, 1360],
    [ 96, 1500],
  ]))

  // ── 내부 벽 추가 영역 ────────────────────────────────────────────────
  // [x1, y1, x2, y2] 형식으로 추가하면 자동으로 벽으로 등록됩니다.
  const innerWalls: [number, number, number, number][] = [
    // x1,  y1,  x2,  y2
  ]
  innerWalls.forEach(([x1, y1, x2, y2]) => result.push({ x1, y1, x2, y2 }))

  return result
})()
